class Product {
  final int id;
  final String name;
  final String description;
  final int price;
  final String image;
  final int yearOfProduction;
  final String fuelType;
  final String fuelConsumption;
  final String engine;

  Product({
    required this.id,
    required this.name,
    required this.description,
    required this.price,
    required this.image,
    required this.yearOfProduction,
    required this.fuelType,
    required this.fuelConsumption,
    required this.engine,
  });
}
